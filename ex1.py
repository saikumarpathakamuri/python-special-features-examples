print("list iteration")
lst=["sandesh","python","java"]
for i in lst:
	print(i)
print("tuple iteration")
tup=("sandesh","python","java")
for i in tup:
	print(i)
print("string iteration")
str="sandesh"
for i in str:
	print(i)
print("set iteration")
set={"sandesh","python","java"}
for i in set:
	print(i)
print("Dictionary iteration")
Dic={"name":"sandesh","sub":"python","sub1":"java"}
for i in Dic:
	print(i,Dic[i])